    function toggleTasksEmptyBody() {
      let tasks = document.querySelectorAll(".task-container");
      if (tasks.length > 0) {
        emptyTasksBody.style.display = "none";
      } else {
        emptyTasksBody.style.display = "flex";
      }
    }

    function toggleTaskSubmitBtn() {
      if (checkTaskTitle()) {
        taskSubmitBtn.classList.remove("submit-btn-disabled");
        taskSubmitBtn.addEventListener("click", toggleSubmitBtnFunctionality);
      } else {
        taskSubmitBtn.classList.add("submit-btn-disabled");
        taskSubmitBtn.removeEventListener(
          "click",
          toggleSubmitBtnFunctionality
        );
      }
    }

    function getTaskInputValues() {
      let taskTitle = taskEditor.children[0].children[0].value;
      let taskDescription = taskEditor.children[0].children[1].value;
      let priorityValue = priority.getAttribute("data-priority");
      let dueDate = dueDateBtn.getAttribute("data-due-date");
      return {
        title: taskTitle,
        description: taskDescription,
        priority: priorityValue,
        task_happen_date: dueDate,
      };
    }

    function getTaskValues(task) {
      return [
        task.querySelector(".task-title").textContent,
        task.querySelector(".task-description").textContent,
        task.querySelector(".check-circle").getAttribute("data-priority"),
        task.querySelector(".due-date-lables").getAttribute("data-due-date"),
      ];
    }

    function clearTaskInputs() {
      taskEditor.children[0].children[0].value = "";
      taskEditor.children[0].children[1].value = "";
      taskEditor.children[0].children[0].focus();
    } 

    function hideTaskEditor() {
      taskEditor.classList.add("d-none");
    }

    function showTaskEditor() {
      taskEditor.classList.remove("d-none");
    }

    function changeTaskEditorRole() {
      if (taskEditor.getAttribute("data-role") == "update") {
        taskEditor.setAttribute("data-role", "post");
      } else {
        taskEditor.setAttribute("data-role", "update");
      }
    }

    function showAddTaskBtn() {
      addTaskBtn.classList.remove("d-none");
      addTaskBtn.classList.add("d-flex");
    }

    function addTask(task) {
      tasksList.append(task);
      clearTaskInputs();
      toggleTasksEmptyBody();
      toggleTaskSubmitBtn();
    }

    async function postTask() {
      let data = getTaskInputValues();
      if (!data.task_happen_date) {
        delete data.task_happen_date;
      }
      let postRequest = await fetch("http://127.0.0.1:8000/tasks/", {
        method: "POST",
        headers: {
          "Content-Type": "application/json",
          Authorization: `JWT ${localStorage.getItem("todoist_access_token")}`,
        },
        body: JSON.stringify(data),
      });
      let response = await postRequest.json();
      addTask(createTask(response));
      clearTaskInputs();
      toggleTasksEmptyBody();
      toggleTaskSubmitBtn();
    }

    function taskEditorCancel() {
      if (taskEditor.getAttribute("data-role") == "post") {
        showAddTaskBtn();
        hideTaskEditor();
      } else {
        taskEditor.nextElementSibling.removeAttribute("style");
        hideTaskEditor();
      }
    }

    function resetTaskEditorValues() {
      taskTitleInput.value = "";
      taskDescriptionInput.value = "";
      dueDateBtn.children[1].textContent = "Due date";
      dueDateBtn.setAttribute("data-due-date", "");
      priority.setAttribute("data-priority", "p4");
      priorityBtn.classList[1] = "p4";
    }

    function loadTaskValuesIntoEditor(task) {
      let values = getTaskValues(task);
      taskTitleInput.value = values[0];
      taskDescriptionInput.value = values[1].slice(1, values[1].length - 1);
      priority.setAttribute("data-priority", values[2]);
      priorityBtn.removeChild(priorityBtn.children[0]);
      priorityBtn.insertAdjacentHTML(
        "afterbegin",
        PRIORITES_SVG[`${values[2]}`]
      );
      priorityBtn.classList.remove(priorityBtn.classList[1]);
      priorityBtn.classList.add(values[2]);
      // swapSelectedIcon("p4", values[2]);
      if (values[3] == "null") {
        dueDateBtn.children[1].textContent = "Due date";
        dueDateBtn.setAttribute("data-due-date", "");
      } else {
        dueDateBtn.setAttribute("data-due-date", values[3]);
        dueDateBtn.children[1].textContent = formatDate(values[3]);
      }
    }

    function formatDate(date) {
      if (date !== undefined) {
        date = date.split("-");
        let year = date[0];
        let month = date[1];
        let day = date[2];
        month = getMonthName(month);
        return `${day} ${month}`;
      } else {
        return "";
      }
    }

    function openUpdateTask(e) {
      if (taskEditor.nextElementSibling.classList.contains("task-container")) {
        taskEditor.nextSibling.removeAttribute("style");
      }
      if (taskEditor.getAttribute("data-role") == "post") {
        changeTaskEditorRole();
      }
      let taskToUpdate = e.target.closest(".task-container");
      taskToUpdate.style.display = "none";
      loadTaskValuesIntoEditor(taskToUpdate);
      swapSelectedIcon(
        priority.getAttribute("data-priority"),
        priority.getAttribute("data-priority")
      );
      showTaskEditor();
      toggleTaskSubmitBtn();
      showAddTaskBtn();
      taskSubmitBtn.textContent = "Save";
      taskSubmitBtn.setAttribute("role", "update");
      tasksList.insertBefore(taskEditor, taskToUpdate);
    }

